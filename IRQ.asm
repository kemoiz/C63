;
; FLASH BACKGROUND/BORDER
;

*=$C000
BORDER  = $D020 ; BORDER
BACK    = $D021 ; BACKGROUND
IRQROUT = $EA31
IRQVEC  = $314
;
NUMBER  = 30 ; EVERY HALF SECOND
;
INIT  SEI
      LDA #<BLINK
      LDY #>BLINK
      STA IRQVEC
      STY IRQVEC+1
      CLI
      RTS
;
BLINK DEC COUNT
      BNE DONE
      LDA #NUMBER
      STA COUNT ; RESET COUNTER
;
; EXCHANGE COLOURS
      LDX BACK
      LDA BORDER
      STA BACK
      STX BORDER
DONE  JMP IRQROUT
;
COUNT BYTE 30

